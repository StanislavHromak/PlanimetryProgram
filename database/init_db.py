import sqlite3
import os


def initialize_database():
    # Шлях до файлу БД (створиться автоматично в папці database)
    db_path = os.path.join(os.path.dirname(__file__), 'theorems.db')

    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()

    # 1. Створення таблиці
    cursor.execute('''
                   CREATE TABLE IF NOT EXISTS theorems
                   (
                       id
                       INTEGER
                       PRIMARY
                       KEY
                       AUTOINCREMENT,
                       figure_type
                       TEXT
                       NOT
                       NULL,
                       name
                       TEXT
                       NOT
                       NULL
                       UNIQUE,
                       formula
                       TEXT,
                       description
                       TEXT
                       NOT
                       NULL
                   )
                   ''')

    # 2. Очищення таблиці перед заповненням (щоб не було дублікатів при повторному запуску)
    cursor.execute('DELETE FROM theorems')

