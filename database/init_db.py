        ("triangle", "Теорема синусів",
         "a/sin(α) = b/sin(β) = c/sin(γ) = 2R",
         "Сторони трикутника пропорційні синусам протилежних кутів. Їх відношення дорівнює діаметру описаного кола."),

        ("triangle", "Формула Герона",
         "S = √(p * (p-a) * (p-b) * (p-c))",
         "Площа трикутника обчислюється через його півпериметр (p) та довжини трьох сторін."),

        ("triangle", "Радіус вписаного кола (трикутник)",
         "r = S / p",
         "Радіус вписаного кола дорівнює відношенню площі трикутника до його півпериметра."),

        ("triangle", "Радіус описаного кола (трикутник)",
         "R = (a*b*c) / (4*S)",
         "Радіус описаного кола дорівнює добутку трьох сторін, поділеному на чотири площі трикутника."),

        # --- ЧОТИРИКУТНИКИ ---
        ("quadrangle", "Площа прямокутника",
         "S = a * b",
         "Площа прямокутника дорівнює добутку його суміжних сторін."),

        ("quadrangle", "Площа трапеції",
         "S = ((a + b) / 2) * h",
         "Площа трапеції дорівнює добутку півсуми її основ на висоту."),

        ("quadrangle", "Площа паралелограма",
         "S = a * h",
         "Площа паралелограма дорівнює добутку його сторони на проведену до неї висоту."),

        # --- КОЛА ТА КРУГИ ---
        ("circle", "Площа круга",
         "S = π * r²",
         "Площа круга дорівнює добутку числа пі на квадрат радіуса."),

        ("circle", "Довжина кола",
         "C = 2 * π * r",
         "Довжина кола дорівнює подвоєному добутку числа пі на радіус."),

